import type { Meta, StoryObj } from '@storybook/react';
import { CalendarHeatmap } from '../../components/ui/charts/CalendarHeatmap';

const sampleData = Array.from({ length: 90 }, (_, i) => {
  const date = new Date(2024, 0, 1 + i);
  const yyyy = date.getFullYear();
  const mm = String(date.getMonth() + 1).padStart(2, '0');
  const dd = String(date.getDate()).padStart(2, '0');
  return { date: `${yyyy}-${mm}-${dd}`, value: Math.round(Math.sin(i * 0.3) * 5 + 5) };
});

const meta: Meta<typeof CalendarHeatmap> = {
  title: 'Charts/CalendarHeatmap',
  component: CalendarHeatmap,
  argTypes: {
    size: { control: 'radio', options: ['sm', 'md', 'lg'] },
    variant: { control: 'select', options: ['default', 'glass', 'bordered', 'elevated'] },
    animated: { control: 'boolean' },
    interactive: { control: 'boolean' },
    showTooltip: { control: 'boolean' },
  },
  decorators: [(Story) => <div className="w-full max-w-2xl p-6"><Story /></div>],
};
export default meta;
type Story = StoryObj<typeof CalendarHeatmap>;

export const Default: Story = { args: { data: sampleData } };
export const Variants: Story = {
  name: 'ðŸŽ¨ Variants',
  render: () => (
    <div className="grid grid-cols-2 gap-6">
      {(['default', 'glass', 'bordered', 'elevated'] as const).map(v => (
        <CalendarHeatmap key={v} data={sampleData} variant={v} title={v} />
      ))}
    </div>
  ),
};
export const DarkMode: Story = {
  name: 'ðŸŒ™ Dark Mode',
  decorators: [(Story) => <div className="dark bg-[#000] p-6 rounded-2xl w-full max-w-2xl"><Story /></div>],
  args: { data: sampleData, variant: 'bordered', title: 'Dark Mode' },
};
export const Playground: Story = {
  name: 'ðŸŽ® Playground',
  args: { data: sampleData, size: 'md', variant: 'default', animated: true, title: 'Playground' },
};
